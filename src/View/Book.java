    public void ShowTableCusTomer() {
        BookController book = new BookController();
        List<BookModel> listBook = book.GetListBook(getBook());
        String[] colHeader = new String[]{"Mã sách", "Tên sách", "Tác giả", "Nhà xuất bản", "Năm xuất bản", "Thể loại", "Giá", "Số lượng"};
        modelTable = new DefaultTableModel(colHeader, 0);
        Object[] rowData = new Object[8];
        for (int i = 0; i < listBook.size(); i++) {
            rowData[0] = listBook.get(i).getBookCode();
            rowData[1] = listBook.get(i).getBookName();
            rowData[2] = listBook.get(i).getAuthor();
            rowData[3] = listBook.get(i).getPublisher();
            rowData[4] = listBook.get(i).getPublishYear();
            rowData[5] = listBook.get(i).getType();
            rowData[6] = listBook.get(i).getPrice();
            rowData[7] = listBook.get(i).getNumber();
            modelTable.addRow(rowData);
        }
        tblBook.setModel(modelTable);
    }

