    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int index = tblBook.getSelectedRow();
        if (tblBook.getRowCount() == 0) {
            showMessage("Khách hàng đang trống, bạn không thể thực hiện sửa!");
            return;
        }
        if (index > -1) {
            BookModel model = new BookModel(
                (String) tblBook.getValueAt(index, 0),
                (String) tblBook.getValueAt(index, 1),
                (String) tblBook.getValueAt(index, 2),
                (String) tblBook.getValueAt(index, 3),
                (String) tblBook.getValueAt(index, 4),
                (String) tblBook.getValueAt(index, 5),
                (Float)  tblBook.getValueAt(index, 6),
                (Integer)tblBook.getValueAt(index, 7));
            new BookInsertOrUpdate(this, 1, model);
        } else {
            showMessage("Bạn hãy chọn sách mình muốn xóa thông tin !");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tblBook.getSelectedRow();
        if (tblBook.getRowCount() == 0) {
            showMessage("Khách hàng đang trống, bạn không thể thực hiện xóa!");
            return;
        }
        //        String mess="Xóa thông tin khách hàng thất bại !";
        if (index >= 0) {
            int reply = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn xóa khách hàng " + tblBook.getValueAt(index, 0) + "?", "Xóa thông tin khách hàng", 0);
            if (reply == JOptionPane.YES_OPTION) {
                String customerCode = (String) tblBook.getValueAt(index, 0);
                BookController customer = new BookController();
                if (customer.BookDelete(customerCode) > 0) {
                    showMessage("Xóa thông tin thành công");
                    ShowTableCusTomer();
                }
            }
        } else {
            showMessage("Bạn hãy chọn sách mình muốn xóa thông tin !");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

