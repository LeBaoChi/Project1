    private boolean Validate() {
        if (txtReaderCode.getText().equals(" ") || txtReaderCode.getText() == null) {
            showMessage("Bạn chưa nhập Mã đọc giả !");
            txtReaderCode.requestFocus();
            return false;
        } else if (txtFullName.getText().equals(" ") || txtFullName.getText() == null) {
            showMessage("Bạn chưa nhập Họ và tên !");
            txtFullName.requestFocus();
            return false;
        } else if (txtAddress.getText().equals(" ") || txtAddress.getText() == null) {
            showMessage("Bạn chưa nhập Địa chỉ !");
            txtAddress.requestFocus();
            return false;
        } else if (txtPhone.getText().equals(" ") || txtPhone.getText() == null) {
            showMessage("Bạn chưa nhập Số điện thoại !");
            txtPhone.requestFocus();
            return false;
        } else if (txtAllowDate.getText().equals(" ") || txtAllowDate.getText() == null) {
            showMessage("Bạn chưa nhập Ngày cấp !");
            txtAllowDate.requestFocus();
            return false;
        } else if (txtDeadLine.getText().equals(" ") || txtDeadLine.getText() == null) {
            showMessage("Bạn chưa nhập Ngày hết hạn !");
            txtDeadLine.requestFocus();
            return false;
        }
        return true;
    }

