    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (Validate()) {
            ReaderModel model = new ReaderModel(
                txtReaderCode.getText(),
                txtFullName.getText(),
                txtAddress.getText(),
                txtPhone.getText(),
                txtEmail.getText(),
                txtAllowDate.getText(),
                txtDeadLine.getText()
            );
            ReaderController book = new ReaderController();
            if (book.ReaderInsertOrUpdate(status, model) > 0) {
                showMessage("Cập nhật thông tin thành công");
                palReader.ShowTableCusTomer();
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

